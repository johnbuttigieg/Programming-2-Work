public class Horse
{


    private String regHorseName;
    private String regHorseColor;
    private int birthYear;


    public String getRegHorseName() {
        return regHorseName;
    }


    public String getRegHorseColor() {
        return regHorseColor;
    }


    public int getBirthYear() {
        return birthYear;
    }

    public void setRegHorseName(String n) {
        regHorseName = n;
    }

    public void setRegHorseColor(String c) {
        regHorseColor = c;
    }

    public void setBirthYear(int y) {
        birthYear = y;
    }
}
